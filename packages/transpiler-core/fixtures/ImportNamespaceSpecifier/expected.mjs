import * as assume from 'node:assert';
import {_power_} from "@power-assert/runtime";
const _pasrt1 = _power_(assume.ok, assume, "assume.ok(truthy)");
const _parg1 = _pasrt1.recorder(0);
const _pasrt2 = _power_(assume.equal, assume, "assume.equal(truthy, falsy)");
const _parg2 = _pasrt2.recorder(0);
const _parg3 = _pasrt2.recorder(1);
const truthy = '1';
const falsy = 0;
_pasrt1.run(_parg1.rec(truthy, 10, 10, 16, 0));
_pasrt2.run(_parg2.rec(truthy, 13, 13, 19, 0), _parg3.rec(falsy, 21, 21, 26, 0));
